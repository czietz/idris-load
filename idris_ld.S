.equ    bootaddr, 0x40000       // target address hard-coded in bootcode

.text
    // check that we are loaded below the target address
    move.l  4(sp),a0
    cmpi.l  #bootaddr,0x18(a0)   // start of BSS = end of this program
    ble.s   load_idris

    // otherwise quit with an error message
    pea     error
    move.w  #9,-(sp)            // Cconws
    trap    #1
    addq.l  #6,sp
    move.w  #0,-(sp)            // Pterm0
    trap    #1

    // copy boot code to target address
load_idris:
    lea     bootcode,a0
    lea     bootaddr,a1
    move.l  a1,a2
    // long-words to copy (minus 1 because of DBRA)
    move.w  #((bootcode_end-bootcode)>>2)-1,d0
copy_loop:
    move.l  (a0)+,(a1)+
    dbra    d0,copy_loop

    pea     0.w
    move.w  #0x20,-(sp)         // Super
    trap    #1

    // mask timer C IRQ so it does not fire while Idris is overwriting the
    // interrupt handler
    bclr.b  #5,0xFFFFFA15.w

    // run bootcode, does not return
    jmp     (a2)

.data

